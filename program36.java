import java.lang.*;

/////////////////////////////////////////////////////////////////
//
//  Class Name :       Program36
//  Description :      Contains main function and Display method
//
/////////////////////////////////////////////////////////////////

class Program36
{
    /////////////////////////////////////////////////////////////////
    //
    //  Function Name :    Display
    //  Description   :    Prints numbers from 1 to iNo using a loop
    //  Input         :    Integer
    //  Output        :    void
    //
    /////////////////////////////////////////////////////////////////

    public static void Display(int iNo)
    {
        int iCnt = 0;                      // Loop counter

        for(iCnt = 1; iCnt <= iNo; iCnt++) // Loop till given number
        {
            System.out.print(iCnt + "\t"); // Print current number
        }
    }

    /////////////////////////////////////////////////////////////////
    //
    //  Entry point function for the application
    //
    /////////////////////////////////////////////////////////////////

    public static void main(String A[])
    {
        Display(7);                        // Function call with argument
    }
}   // End of class

