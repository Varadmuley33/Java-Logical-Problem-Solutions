import java.lang.*;

/////////////////////////////////////////////////////////////////
//
//  Class Name :       Program33
//  Description :      Contains main function and Display method
//
/////////////////////////////////////////////////////////////////

class Program33
{
    /////////////////////////////////////////////////////////////////
    //
    //  Function Name :    Display
    //  Description   :    Prints pattern: number followed by '*' using while loop
    //
    /////////////////////////////////////////////////////////////////

    public static void Display()
    {
        int iCnt = 1;                      // Initialization

        while(iCnt <= 5)                   // Loop condition
        {
            System.out.print(iCnt + "\t*\t");     // Print number and star
            iCnt++;                        // Increment
        }
    }

    /////////////////////////////////////////////////////////////////
    //
    //  Entry point function for the application
    //
    /////////////////////////////////////////////////////////////////

    public static void main(String A[])
    {
        Display();                         // Function call
    }
}   // End of class

