import java.lang.*;

/////////////////////////////////////////////////////////////////
//
//  Class Name :       Program35
//  Description :      Contains main function and Display method
//
/////////////////////////////////////////////////////////////////

class Program35
{
    /////////////////////////////////////////////////////////////////
    //
    //  Function Name :    Display
    //  Description   :    Prints numbers 5 to 1 using a decreasing for loop
    //
    /////////////////////////////////////////////////////////////////

    public static void Display()
    {
        int iCnt = 0;                      // Loop counter

        for(iCnt = 5; iCnt >= 1; iCnt--)   // Reverse loop
        {
            System.out.print(iCnt + "\t"); // Print number
        }

        System.out.println();              // New line
    }

    /////////////////////////////////////////////////////////////////
    //
    //  Entry point function for the application
    //
    /////////////////////////////////////////////////////////////////

    public static void main(String A[])
    {
        Display();                         // Function call
    }
}   // End of class

