class program2
{
    public static void main(String args[])
    {
        int    iValue = 10;         // Integer variable
        char   cValue = 'V';        // Character variable
        double dValue = 55.89;      // Double variable

        System.out.println("Integer Value : " + iValue);
        System.out.println("Character Value : " + cValue);
        System.out.println("Double Value : " + dValue);

    }   // End of main
}

