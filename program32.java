import java.lang.*;

/////////////////////////////////////////////////////////////////
//
//  Class Name :       Program32
//  Description :      Contains main function and Display method
//
/////////////////////////////////////////////////////////////////

class Program32
{
    /////////////////////////////////////////////////////////////////
    //
    //  Function Name :    Display
    //  Description   :    Prints pattern: 1 * 2 * 3 * 4 * 5 *
    //
    /////////////////////////////////////////////////////////////////

    public static void Display()
    {
        System.out.print("1\t*\t");
        System.out.print("2\t*\t");
        System.out.print("3\t*\t");
        System.out.print("4\t*\t");
        System.out.print("5\t*\t");            // Pattern output
    }

    /////////////////////////////////////////////////////////////////
    //
    //  Entry point function for the application
    //
    /////////////////////////////////////////////////////////////////

    public static void main(String A[])
    {
        Display();                             // Function call
    }
}   // End of class

